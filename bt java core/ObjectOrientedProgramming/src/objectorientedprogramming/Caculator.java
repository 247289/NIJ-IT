public class Caculator {
    public float add(float a, float b){
        return a+b;
    }
    public float minus(float a, float b){
        return a-b;
    }
    public float devide(float a, float b){
        return a/b;
    }
    public float multiple(float a, float b) {
        return a*b;
    }
}
